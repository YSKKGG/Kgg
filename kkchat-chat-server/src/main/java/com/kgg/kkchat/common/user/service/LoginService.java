package com.kgg.kkchat.common.user.service;

/**
 * Description:
 * Author: Kgg
 * Date: 2023/12/24
 */
public interface LoginService {

    /**
     * 刷新token有效期
     *
     * @param token
     */
    void renewalTokenIfNecessary(String token);

    /**
     * 登录成功，获取token
     *
     * @param uid
     * @return 返回token
     */
    String login(Long uid);

    /**
     * 如果token有效，返回uid
     *
     * @param token
     * @return
     */
    Long getValidUid(String token);

}
